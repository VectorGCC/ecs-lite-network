using System.Text;
using UnityCodeGen;
using UnityEditor;

[Generator]
public class SystemAutoCollectGenerator : ICodeGenerator
{
    public void Execute(GeneratorContext context)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("using Leopotam.EcsLite;");
        sb.AppendLine("public partial class SystemAutoCollect : SystemAutoCollectBase");
        sb.AppendLine("{");
        sb.AppendLine("public override void AddAutoCollectSystems(EcsSystems systems)");
        sb.AppendLine("{");

        var types = TypeCache.GetTypesWithAttribute<SystemAutoCollectAttribute>();
        foreach (var type in types)
        {
            var name = type.FullName;
            sb.AppendLine("systems.Add(new " + name + "());");
        }

        sb.AppendLine("}");
        sb.AppendLine("}");

        context.AddCode("SystemAutoCollect.Generated.cs", sb.ToString());
    }
}